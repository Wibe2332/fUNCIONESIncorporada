let cadena2 = "Mundo";
let cadena3 = cadena1 + ' ' // "Holamundo"

console.log(cadena3); // "Holamundo"

let cadena4 = cadena1 + " " + cadena2; // "Hola Mundo"
console.log(cadena4); // "Hola Mundo"

